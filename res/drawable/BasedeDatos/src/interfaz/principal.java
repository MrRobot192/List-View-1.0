            // TODO add your handling code here:
            PreparedStatement ps = cn.prepareStatement("INSERT INTO tblusuarios(usuario,nombre,edad,email,marca)"
                                    + " VALUES(?,?,?,?,?)");
            ps.setString(1, txtUsuario.getText());
            ps.setString(2, txtNombre.getText());
